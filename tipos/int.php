<?php 

echo 11; //inteiro
echo '<br>';

var_dump(11); //var_dump mostra o tipo de variável 
echo '<br>';

echo PHP_INT_MAX, '<br>'; //PHP_INT_MAX motra o maior valor inteiro possível no PHP
echo PHP_INT_MIN, '<br>'; //PHP_INT_MIN mostra o menor valor inteiro possível no PHP
echo 017, '<br>'; //0 antes do número indica que é um número octal
echo 0b11000111001, '<br>'; //0b antes do número indica que é um número binário
echo 0x117acf0; //0x antes do número indica que é um número hexadecimal